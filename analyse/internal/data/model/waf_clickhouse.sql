CREATE TABLE sec_log (
                         log_id String,
                         uri String,
                         trans_id String,
                         ctime DateTime,
                         protocol String,
                         request String,
                         req_method String,
                         response String,
                         client_ip String,
                         rule_name String,
                         rule_desc String,
                         rule_group_name String,
                         server_group_name String,
                         server_name String,
                         app_name String,
                         strategy String,
                         action String,
                         next_action String,
                         client_port Int32,
                         alert_level Int32,
                         server_group_id Int32,
                         rule_group_id Int32,
                         rule_id Int32
) ENGINE = MergeTree()
      ORDER BY (log_id, ctime);